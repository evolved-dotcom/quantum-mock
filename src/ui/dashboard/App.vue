  notifyBackground();
};

const exportHub = () => {
  try {
    const payload = JSON.stringify(rules.value);
    const base64 = btoa(unescape(encodeURIComponent(payload)));
    navigator.clipboard.writeText(base64);
    exportToast.value = true;
    setTimeout(() => { exportToast.value = false; }, 3000);
  } catch(e) {
    alert("Failed to export rules.");
  }
};

const importHub = async () => {
  if (!importData.value) return;
  try {
    const jsonStr = decodeURIComponent(escape(atob(importData.value.trim())));
    const importedRules: MockRule[] = JSON.parse(jsonStr);
    
    for (const r of importedRules) {
      r.id = crypto.randomUUID(); 
      await db.mockRules.put(r);
    }
    
    await loadRules();
    notifyBackground();
    showImport.value = false;
    importData.value = '';
    alert(`Successfully injected ${importedRules.length} rules!`);
  } catch (e) {
    alert("Invalid Config Code. Ensure you copied the full Base64 string.");
  }
};

