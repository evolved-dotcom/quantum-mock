<script setup lang="ts">
import { ref, onMounted, computed, watch } from 'vue';
import { db } from '../../db/db';
import InspectorDictView from './components/InspectorDictView.vue';
import InspectorPayload from './components/InspectorPayload.vue';
import type { MockRule } from '../../schema/mockRule';
import JsonViewer from './components/JsonViewer.vue';
import StatusCodeSelect from './components/StatusCodeSelect.vue';
import CyberSelect from './components/CyberSelect.vue';

const rules = ref<MockRule[]>([]);
const selectedRule = ref<MockRule | null>(null);
const selectedIds = ref<string[]>([]);
const isSaving = ref(false);
const isRecording = ref(false);
const jsonViewMode = ref(false);

const rightPanelMode = ref<'editor' | 'inspector'>('editor');
const inspectorTab = ref('reqPayload');

const inspectorTabs = [
  { id: 'reqPayload', label: 'Request Payload' },
  { id: 'resPayload', label: 'Response Payload' },
  { id: 'reqHeaders', label: 'Request Headers' },
  { id: 'resHeaders', label: 'Response Headers' },
  { id: 'cookies', label: 'Cookies' }
];

const tooltipState = ref({
  visible: false,
  x: 0,
  y: 0,
  title: '',
  desc: ''
});

const onShowTooltip = ({ entry, event }: any) => {
  tooltipState.value = {
    visible: true,
    x: event.clientX,
    y: event.clientY,
    title: entry.title,
    desc: entry.desc
  };
};

const onHideTooltip = () => {
  tooltipState.value.visible = false;
};

const activeInspectorComponent = computed(() => {
  if (inspectorTab.value.includes('Payload')) return InspectorPayload;
  return InspectorDictView;
});

const inspectorProps = computed(() => {
  const traffic = selectedRule.value?.capturedTraffic;
  if (!traffic) return { title: 'No Data' };
  
  switch(inspectorTab.value) {
    case 'reqPayload': return { title: 'Request Payload', body: traffic.requestBody || '' };
    case 'resPayload': return { title: 'Response Payload', body: selectedRule.value?.responses[0]?.body || '' };
    case 'reqHeaders': return { title: 'Request Headers', data: traffic.requestHeaders || {} };
    case 'resHeaders': return { title: 'Response Headers', data: traffic.responseHeaders || {} };
    case 'cookies': return { title: 'Cookies', data: traffic.cookies || {} };
    default: return { title: 'Unknown' };
  }
});

const groupedRules = computed(() => {
  const groups: Record<string, Record<string, MockRule[]>> = {};
  const order = ['GET', 'POST', 'PUT', 'PATCH', 'DELETE', 'OPTIONS', 'ANY'];
  
  for (const rule of rules.value) {
    if (!groups[rule.method]) groups[rule.method] = {};
    
    let rawUrl = rule.urlPattern?.trim() || '';
    let basePath = rawUrl.split('?')[0];
    
    if (basePath !== '*' && basePath !== '.*' && basePath !== '') {
      try {
        if (basePath.startsWith('http')) {
          const u = new URL(basePath);
          const parts = u.pathname.split('/').filter(Boolean);
          const groupSegments = parts.slice(0, 2);
          basePath = u.origin + (groupSegments.length ? '/' + groupSegments.join('/') : '');
        } else {
          const parts = basePath.split('/').filter(Boolean);
          const groupSegments = parts.slice(0, 2);
          basePath = groupSegments.length ? '/' + groupSegments.join('/') : basePath;
        }
      } catch(e) {
        const parts = basePath.split('/');
        basePath = parts.slice(0, 3).join('/');
      }
    }
    
    if (!groups[rule.method][basePath]) groups[rule.method][basePath] = [];
    groups[rule.method][basePath].push(rule);
  }
  
  const sortedGroups: Record<string, Record<string, MockRule[]>> = {};
  order.forEach(method => {
    if (groups[method]) sortedGroups[method] = groups[method];
  });
  
  return sortedGroups;
});

const parsedJsonBody = computed(() => {
  if (!selectedRule.value) return null;
  const activeRes = selectedRule.value.responses[selectedRule.value.activeResponseIndex || 0];
  if (!activeRes || !activeRes.body) return null;
  try {
    return JSON.parse(activeRes.body);
  } catch {
    return null;
  }
});

watch(parsedJsonBody, (newVal) => {
  if (newVal === null && jsonViewMode.value) {
    jsonViewMode.value = false;
  }
});

const showImport = ref(false);
const importData = ref('');
const exportToast = ref(false);

const loadRules = async () => {
  const rawRules = await db.mockRules.toArray();
  const migratedRules: MockRule[] = [];
  
  for (const r of rawRules) {
    if (!r.responses && (r as any).response) {
      r.responses = [{
        ...((r as any).response),
        delayMs: (r as any).delayMs || 0,
        simulateNetworkError: (r as any).simulateNetworkError || false
      }];
      delete (r as any).response;
      delete (r as any).delayMs;
      delete (r as any).simulateNetworkError;
      r.activeResponseIndex = 0;
      await db.mockRules.put(r);
    }
    if (r.activeResponseIndex === undefined) {
      r.activeResponseIndex = 0;
    }
    migratedRules.push(r as MockRule);
  }
  
  rules.value = migratedRules;
};

const loadRecordingState = async () => {
  const result = await chrome.storage.local.get('isRecording');
  isRecording.value = !!result.isRecording;
};

const toggleRecording = async () => {
  await chrome.storage.local.set({ isRecording: isRecording.value });
};

onMounted(() => {
  loadRules();
  loadRecordingState();
});

chrome.storage.onChanged.addListener((changes, area) => {
  if (area === 'local' && changes.isRecording) {
    isRecording.value = !!changes.isRecording.newValue;
  }
});

